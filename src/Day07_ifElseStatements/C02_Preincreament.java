package Day07_ifElseStatements;

public class C02_Preincreament {
    public static void main(String[] args) {
        int sayi=10;
        System.out.println(sayi);
        sayi++;
        System.out.println(sayi);

        sayi++;
        System.out.println(sayi);
        System.out.println(++sayi);
        System.out.println(sayi);




    }
}
